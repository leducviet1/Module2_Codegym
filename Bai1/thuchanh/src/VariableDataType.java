public class VariableDataType {
    public static void main(String[] args) {
        int i = 10;
        float f = 20.5f;
        double d = 30.5;
        char c = 'A';
        boolean b = true;
        String s = "Hello";

        System.out.println("i = " + i);
        System.out.println("f = " + f);
        System.out.println("d = " + d);
        System.out.println("c = " + c);
        System.out.println("b = " + b);
        System.out.println("s = " + s);
    }
}
